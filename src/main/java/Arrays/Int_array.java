package Arrays;

public class Int_array {
    public static void main(String[] args) {
        int []num={45,60,66,80,90,100};

        for (int i=0;i<num.length;i++){
            System.out.println(num[i]);

            for(int j=5;j>=0;j--){
                System.out.println(num[j]);
            }
        }
    }
}
